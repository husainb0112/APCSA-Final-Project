// MatchingActivity.java
// Pairing terms and definitions for practice
public class MatchingActivity {
    private String[] terms;
    private String[] definitions;

    public MatchingActivity(String[] t, String[] d) {
        terms = t;
        definitions = d;
    }
    public String[] getTerms() { 
        return terms; 
    }
    public String[] getDefinitions() { 
        return definitions; 
    }
}
